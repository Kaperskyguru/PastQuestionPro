    ActionListener checkClientCurrentTime = new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
            countSeconds--;
            lblTime.setText(count1 + ":" + countSeconds);
            if (countSeconds == 0 && count1 != 0) {
                count1--;
                countSeconds = 60;
            } else if (count1 == 0 && countSeconds == 0) {
                RegulateTime.stop();

            }

        }
    };
